//Array operations
//1. Push

week = ['monday','Tuesday','wednesday']
week.push('Thursday' , 'Friday' , 'saturday')
console.log(week);

console.log(`-------------------------------------`);


//2. unshift

week.unshift('sunday')
console.log(week);
console.log(`-------------------------------------`);

//3.pop
week.pop()
console.log(week);
console.log(`-------------------------------------`);

//4.shift
week.shift()
console.log(week);

